import turtle
sc=turtle.Screen()
sc.bgcolor("pink")
sc.setup(width=600, height=600)
turtle.title("My turtle Drawing")
t=turtle.Turtle()
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.penup()
t.right(150)
t.forward(50)
t.pendown()
t.right(90)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
turtle.done()