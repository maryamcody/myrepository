num1= 10
num2= 4
#Using AND operation
print("num1 & num2 =", num1 & num2)

#Using OR operation
print ("num1| num2 =", num1 | num2)

#using NOt operation
print("\n~num1=", ~num1)

#Using XOR operation
print("\nnum1^ num2=", num1^num2)

#Using Right Shift operation 
print("\nnum1>>1 =", num1>>1)
print("\nnum2>>1=", num2>>1)

#Using Left Shift operation
print("\nnum1<<1= ", num1<<1)
print("\nnum2<<1= ", num2<<1)
